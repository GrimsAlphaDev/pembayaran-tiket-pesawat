    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_data.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih Baris yang akan diperbarui");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda Yakin Ingin Menghapus Data ini ? ", "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            
            String ticket_id = tbl_data.getValueAt(selectedRow, 1).toString();
            String ticket_number = tbl_data.getValueAt(selectedRow, 2).toString();

            try {
                String sql = "DELETE FROM payments WHERE ticket_id = ?";
                PreparedStatement st = conn.prepareStatement(sql);
                st.setString(1, ticket_id);

                int rowDelete = st.executeUpdate();
                if (rowDelete > 0) {

                    String sql1 = "DELETE FROM tickets WHERE ticket_number = ?";
                    PreparedStatement st1 = conn.prepareStatement(sql1);
                    st1.setString(1, ticket_number);

                    int rowDelete1 = st1.executeUpdate();
                    
                    if(rowDelete1 > 0) {
                        JOptionPane.showMessageDialog(this, "Tiket Berhasil Dihapus");
                    } else {
                        JOptionPane.showMessageDialog(this, "Tiket Gagal Dihapus");
                    }
                    st1.close();
                }

                st.close();
            } catch (SQLException e) {
                Logger.getLogger(UserTickets.class.getName()).log(Level.SEVERE, null, e);
            }
        }
        reset();
        getData();
        nonAktif();
    }//GEN-LAST:event_btn_deleteActionPerformed

