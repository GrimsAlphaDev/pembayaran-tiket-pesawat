    private void searchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyTyped
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbl_data.getModel();
        model.setRowCount(0);

        String cari = search.getText();
        int customerId = session.getuserId();

        try {
            String sql = "SELECT t.id, t.user_id, t.flight_id, t.ticket_number, t.booking_time, "
                    + "f.flight_number, f.departure, f.destination, f.departure_time, f.arrival_time, f.price "
                    + "FROM tickets t "
                    + "JOIN flights f ON t.flight_id = f.id "
                    + "WHERE t.flight_id = f.id AND t.user_id = ? AND (ticket_number LIKE ? OR flight_number LIKE ? OR departure LIKE ? OR destination LIKE ? OR departure_time LIKE ? OR arrival_time LIKE ? OR price LIKE ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, customerId);
            String cariWildcard = "%" + cari + "%";
            for (int i = 2; i <= 8; i++) {
                ps.setString(i, cariWildcard);
            }
            ResultSet rs = ps.executeQuery();

            int no = 1;

            while (rs.next()) {
                int ticketId = rs.getInt("id");
                String ticket_num = rs.getString("ticket_number");
                String flightNumber = rs.getString("flight_number");
                String departure = rs.getString("departure");
                String destination = rs.getString("destination");
                String departure_time = rs.getString("departure_time");
                String arrival_time = rs.getString("arrival_time");
                int price = rs.getInt("price");

                Object[] row = {no,ticketId, ticket_num, flightNumber, departure, destination, departure_time, arrival_time, price};
                model.addRow(row);
                no++;
            }
            ps.close();
            rs.close();

        } catch (SQLException e) {
            Logger.getLogger(UserDashboardForm.class.getName()).log(Level.SEVERE, "Koneksi ke database gagal", e);
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memuat Jadwal Penerbangan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchKeyTyped

